@interface NoteTableViewController ()

@property (strong, nonatomic) NSMutableArray *dataSource;

@end

@implementation NoteTableViewController

- (void)viewDidLoad {
    [super viewDidLoad];
    
    typeof(self) __weak __block weakSelf = self;
    [NoteConnectionManager getNotesWithCompletionBlock:^(NSMutableArray *result) {
        weakSelf.dataSource = result;
        dispatch_async(dispatch_get_main_queue(), ^{
            [weakSelf.tableView reloadData];
        });
    }];
    
    
    // Uncomment the following line to preserve selection between presentations.
